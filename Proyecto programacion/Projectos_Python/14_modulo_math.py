import math

#math.ceil() devuelve el numero entero superior mas cercano
print(math.ceil(1.2))
print(math.ceil(5.9))

#math.floor() devuelve el numero entero inferior mas cercano
print(math.floor(1.9))
print(math.floor(2.3))

#evalua si no es un numero, solo para numeros reales
print(math.isnan(23))
print(math.isnan(-23))
print(math.isnan(5.3))
#print(math.isnan("23"))

print(math.pow(10, 3))#eleva un numero a otro
print(math.sqrt(9)) #raiz cuadrada